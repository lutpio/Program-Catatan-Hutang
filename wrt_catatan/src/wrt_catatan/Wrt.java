    private void BTN_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_saveActionPerformed
        String cid_CHOICE = CHOICE_customer.getSelectedItem();        
        
        if(FIELD_debt.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Tolong isi utangnya");
        }else if(FIELD_debt.getText().substring(0, 1).equals("0")){
            JOptionPane.showMessageDialog(null,"Tolong isi yang benar");
            FIELD_debt.setText(null);
        }
        else{
            int confirm = JOptionPane.showConfirmDialog(this,"Yakin ingin catat?","Konfirmasi",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            Integer duitINT = Integer.valueOf(FIELD_debt.getText());
            if (confirm == JOptionPane.YES_OPTION){
                cid = crud.find_cid(cid_CHOICE);
                if(FIELD_catatan.getText().equals("")){
                    crud.add_transaction(cid, duitINT, dt);
                }else{
                    crud.add_transaction(cid, duitINT, FIELD_catatan.getText(), dt);    
                }
                JOptionPane.showMessageDialog(null,"Sukses");
                stat="belum lunas";
                crud.modify_stat(cid,stat);
            }else if(confirm == JOptionPane.NO_OPTION){
                FIELD_debt.setText(null);
                FIELD_catatan.setText(null);
            }
            
        }

    }//GEN-LAST:event_BTN_saveActionPerformed

