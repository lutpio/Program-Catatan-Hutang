    private void TABLE_debtMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TABLE_debtMouseClicked
        int baris = TABLE_debt.rowAtPoint(evt.getPoint());
        stat = TABLE_debt.getValueAt(baris, 1).toString();
        cid = crud.find_cid(TABLE_debt.getValueAt(baris, 0).toString());
        if(evt.getClickCount() == 1){
            if(stat.equals("lunas")){
                BTN_hapus.setEnabled(true);                        
            }else{
                BTN_hapus.setEnabled(false); 
            }
        }
        else if(evt.getClickCount() == 2){
            jTabbedPane1.setSelectedIndex(3);
            BTN_lunas.setVisible(false);
            BTN_belum.setVisible(false);
            BTN_semua.setVisible(false);
            
            
            
            
            sisa = crud.sisa(cid);
            
            displayData(cid);
            String hp = crud.find_phone(cid);
            String sisaSTR = String.valueOf(sisa);
            LABEL_customer.setText(TABLE_debt.getValueAt(baris, 0).toString());
            LABEL_nohp.setText(hp);
            LABEL_sisa.setText(sisaSTR);
            
            if(stat.equals("lunas")){
                BTN_bayar.setEnabled(false);
            }else{
                BTN_bayar.setEnabled(true);
            }
            
        }
    }//GEN-LAST:event_TABLE_debtMouseClicked

