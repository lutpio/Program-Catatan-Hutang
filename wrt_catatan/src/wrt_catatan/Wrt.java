    private void BTN_bayar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTN_bayar2ActionPerformed
        if(FIELD_bayar.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Tolong isi utangnya");
        }else if(FIELD_bayar.getText().substring(0, 1).equals("0")){
            JOptionPane.showMessageDialog(null,"Tolong isi yang benar");
            FIELD_bayar.setText(null);
        }else{
            int confirm = JOptionPane.showConfirmDialog(this,"Yakin ingin catat?","Konfirmasi",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
            Integer bayarINT = Integer.valueOf(FIELD_bayar.getText());
            if (confirm == JOptionPane.YES_OPTION){
                if(bayarINT > sisa){
                    bayarINT = sisa;
                    LABEL_sisa2.setText("0");
                }

                cid = crud.find_cid(LABEL_customer2.getText());
                crud.add_transaction(cid, "pelunasan", bayarINT,dt);   
                sisa = crud.sisa(cid);                          
                String sisaSTR = String.valueOf(sisa);
                JOptionPane.showMessageDialog(null,"Sukses");
                LABEL_sisa2.setText(sisaSTR);
                
            }else if(confirm == JOptionPane.NO_OPTION){
                FIELD_bayar.setText(null);                
            }
           
        }
        
    }//GEN-LAST:event_BTN_bayar2ActionPerformed

